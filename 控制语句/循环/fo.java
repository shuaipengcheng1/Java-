package 循环;

public class fo {
    public static void main(String[] args) {
//        循环 与 js一样·1
//        打印出99表
        for(int i=1;i<=9;i++){
            System.out.println('\n');
            for(int j=1;j<=9;j++){
                System.out.print(i+"*"+j+"="+ i*j+" ");
            }
        }
    }
}
